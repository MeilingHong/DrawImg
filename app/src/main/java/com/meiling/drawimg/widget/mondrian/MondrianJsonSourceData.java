package com.meiling.drawimg.widget.mondrian;

public class MondrianJsonSourceData {

    int x;

    int y;

    int w;

    int h;

    String type;

    int addressIndex;

    public MondrianJsonSourceData(int x, int y, int w, int h, String type, int addressIndex) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
        this.type = type;
        this.addressIndex = addressIndex;
    }
}
